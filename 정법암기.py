                # 선택된 키워드 강조 스타일
                is_selected = st.session_state.selected_keyword == kw
                label = f"📍 {kw['text']}" if is_selected else kw['text']
                type_style = "primary" if is_selected else "secondary"
                
                if st.button(label, key=f"kw_{idx}", type=type_style, use_container_width=True):
                    st.session_state.selected_keyword = kw
